package com.jntucep.c19_delhi;

import android.content.Intent;
import android.location.Address;
import android.location.Geocoder;
import android.os.Bundle;
import android.view.View;
import android.widget.Button;
import android.widget.EditText;

import com.google.android.gms.maps.CameraUpdateFactory;
import com.google.android.gms.maps.GoogleMap;
import com.google.android.gms.maps.OnMapReadyCallback;
import com.google.android.gms.maps.SupportMapFragment;
import com.google.android.gms.maps.model.LatLng;
import com.google.android.gms.maps.model.MarkerOptions;
import com.google.firebase.auth.FirebaseAuth;
import com.google.firebase.database.DatabaseReference;
import com.google.firebase.database.FirebaseDatabase;

import java.io.IOException;
import java.util.List;

import androidx.fragment.app.FragmentActivity;

public class MapsActivityvol extends FragmentActivity implements OnMapReadyCallback {

    private GoogleMap mMap;
    private Button frontbutton;
    private Button backbutton;
    EditText slocation;
    DatabaseReference reff;
    FirebaseAuth mAuth = FirebaseAuth.getInstance();

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_mapsvol);
        slocation=(EditText)findViewById(R.id.searchlocation);
        reff = FirebaseDatabase.getInstance().getReference().child("Events").child(mAuth.getUid()).child(getIntent().getStringExtra("EventId")).child("location");

        // Obtain the SupportMapFragment and get notified when the map is ready to be used.
        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
        frontbutton = (Button) findViewById(R.id.FinalStep);
        frontbutton.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                reff.setValue(slocation.getText().toString());
                Intent i = new Intent(view.getContext(), HostFinal.class);
                startActivity(i);
            }
        });
        backbutton = (Button) findViewById(R.id.GoBack);
        backbutton.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                Intent i = new Intent(view.getContext(), HostAnEvent.class);
                startActivity(i);
            }
        });
    }
    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;
        LatLng sydney = new LatLng(-34, 151);
        mMap.addMarker(new MarkerOptions().position(sydney).title("Marker in Sydney"));
        mMap.moveCamera(CameraUpdateFactory.newLatLng(sydney));
    }
    public void onMapSearch(View view) {
        EditText locationSearch = (EditText) findViewById(R.id.searchlocation);
        String location = locationSearch.getText().toString();
        List<Address> addressList = null;

        if (location != null || !location.equals("")) {
            Geocoder geocoder = new Geocoder(this);
            try {
                addressList = geocoder.getFromLocationName(location, 1);

            } catch (IOException e) {
                e.printStackTrace();
            }
            if(addressList.isEmpty()==true)
            {
                locationSearch.requestFocus();
                locationSearch.setError("Invalid location");
            }
            else {
                Address address = addressList.get(0);
                LatLng latLng = new LatLng(address.getLatitude(), address.getLongitude());
                mMap.addMarker(new MarkerOptions().position(latLng).title("Marker"));
                mMap.animateCamera(CameraUpdateFactory.newLatLng(latLng));
            }
        }
    }


}